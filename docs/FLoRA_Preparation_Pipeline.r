clean_doi <- function(x) {
  x <- tolower(trimws(as.character(x)))
  x <- gsub("^(https?://)?(dx\\.)?doi\\.org/", "", x)
  x <- gsub("^doi:\\s*", "", x)
  x <- sub("\\s*digital\\s*object\\s*identifier.*$", "", x)
  x <- sub("\\s*get\\s*rights\\s*and\\s*content.*$", "", x)
  x <- sub("\\s.*$", "", x)
  x <- trimws(x)
  dplyr::na_if(x, "")
}

