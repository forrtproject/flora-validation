norm_url <- function(x) {
  x <- tolower(trimws(as.character(x)))
  x <- gsub("^https?://", "", x)
  x <- gsub("^www\\.", "", x)
  x <- gsub("/+$", "", x)
  dplyr::na_if(x, "")
}

